package basic;
public class App {
    // 程式進入點: 一律找main
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!123");
        System.out.println("Hello, World123!");

// mac  
// command + c : 
// 複製,command  + v : 
// 貼上,command  + a :  
// 全選,command + s :  

// win   
// 儲存 Crtl+C : 
// 複製,Ctrl+V : 
// 貼上,Crtl+A : 
// 全選,Crtl+S :

// 類別名稱要跟擋案名稱相同
// 類別名稱要大寫開頭
// {} : 左右大括號
// [] : 左右中括號
// () : 左右小括號
// 類別 -> 方法 -> 每一行程式碼
// 系統的輸出的換行印出
        // 每一行結束要有分號
        System.out.println("Hello, World!");
        System.out.println("hi Edward1");
        System.out.println("hi Edward2");

        // 變數宣告: 在記憶體規劃出一個櫃子儲存數值
        // = : 右邊數值丟到左邊容器

        // 不用變數
        System.out.println(5 + 3);
        System.out.println(5 * 3);
        System.out.println(5 / 3);
        System.out.println(5 - 3);
        // 使用變數
        int x = 2;
        int z = 3;
        System.out.println(x + z);
        System.out.println(x * z);
        System.out.println(x / z);
        System.out.println(x - z);

        String y1 = "甲方";
        String y2 = "丙方";
        String y3 = "你好";

        System.out.println(y1 + y2 + y3);
    }


}

